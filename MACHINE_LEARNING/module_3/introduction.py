# Construcción de modelos de Aprendizaje supervisado

# En este módulo, aprenderá a implementar modelos modernos de aprendizaje automático supervisado.
# Empezará entendiendo cómo funciona la clasificación binaria y descubrirá cómo construir un clasificador
# multiclase a partir de componentes de clasificación binaria. Aprenderá qué son los Árboles de decisiones,
# cómo aprenden y cómo construirlos. Los árboles de decisiones, que se utilizan para resolver problemas de clasificación,
# tienen una extensión natural llamada árboles de regresión, que pueden tratar problemas de regresión.
# Aprenderá sobre otros modelos de Aprendizaje supervisado, como KNN y SVM. Aprenderá qué son el sesgo y
# la varianza en el ajuste de modelos y el equilibrio entre sesgo y varianza que es inherente a todos los
# modelos de aprendizaje en diversos grados. Aprenderá estrategias para mitigar esta compensación y trabajará
# con modelos que hacen un muy buen trabajo logrando ese objetivo.

