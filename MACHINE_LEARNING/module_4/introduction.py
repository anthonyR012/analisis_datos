# Creación de modelos de Aprendizaje no supervisado

# En este módulo, se sumergirá en el Aprendizaje no supervisado,
# donde los algoritmos descubren patrones en los datos sin ejemplos etiquetados.
# Explorará estrategias de clustering y aplicaciones reales, centrándose en
# técnicas como el clustering jerárquico, k-means y métodos avanzados como DBSCAN y HDBSCAN.
# A través de laboratorios prácticos, obtendrá una comprensión más profunda de cómo comparar
# e implementar estos algoritmos de manera efectiva. Además, profundizará en algoritmos de
# reducción de dimensiones como PCA (Análisis de componentes principales), t-SNE y UMAP
# para reducir las características del conjunto de datos y simplificar otras tareas de modelado.
# Utilizando Python, implementará estas técnicas de agrupamiento y reducción de dimensionalidad,
# aprendiendo a integrarlas con la ingeniería de características para preparar los datos
# para los modelos de aprendizaje automático.